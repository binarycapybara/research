#include "libft.h"

size_t		ft_n_len(long nb)
{
	size_t		ln;

	ln = 1;
	if (nb < 0)
		ln++;
	while (nb / 10 != 0)
	{
		ln++;
		nb /= 10;
	}
	return (ln);
}
